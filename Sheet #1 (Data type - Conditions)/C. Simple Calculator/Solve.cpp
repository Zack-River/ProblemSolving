#include <bits/stdc++.h>
 
using namespace std;
 
int main()
{
    long long mul;
    int n1;
    long long n2;
    cin >> n1 >> n2;
    mul = n1 * n2;
    cout << n1 << " + " << n2 << " = " << n1 + n2 << "\n";
    cout << n1 << " * " << n2 << " = " << mul << "\n";
    cout << n1 << " - " << n2 << " = " << n1 - n2;
    return 0; 
}